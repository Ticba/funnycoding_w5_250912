# 函式遞迴 recursive

# 吩咐的順序：你（百寶袋）-> s 號的你 -> ss 號的你 -> Xs 號的你（沒有百寶袋）
# 執行完任務的順序：Xs -> ss -> s -> 你

def f(n):
    if n == 0:
        return 0
    return f(n-1) + 1

print(f(3)) 

# f(3): return f(2) + 1  ---等待 f(2)-->   f(2): return f(1) + 1  ---等待 f(1)-->   f(1): return f(0) + 1  ---等待 f(0)-->   f(0): return 0
# 結束順序
#  -> f(0) = 0
#  -> f(1) = f(0) + 1 = 0 + 1 = 1
#  -> f(2) = f(1) + 1 = 1 + 1 = 2
#  -> f(3) = f(2) + 1 = 2 + 1 = 3