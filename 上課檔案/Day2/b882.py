# input: 22 20 142
# output: 22:22:22

# 60  秒是 1 分鐘
# 144 秒是 ? 分鐘，又多了 ? 秒  (取商數, 取餘數)

h,m,s = input().split()
h, m, s = int(h), int(m), int(s)

s_q, s_r = s // 60, s%60   # 輸出第三個數字為 s_r

# m 要先加上 s_q
# 60  分是 1 小時
# 144 分是 ? 小時，又多了 ? 分  (取商數, 取餘數)

m += s_q
m_q, m_r = m // 60, m%60   # 輸出第二個數字為 m_r

# h 要加上 m_q
# h % 24

h += m_q
h %= 24                   # 輸出第一個數字為 h

print(f'{h:02}:{m_r:02}:{s_r:02}')